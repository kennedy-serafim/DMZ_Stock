    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        if (tableEmail.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Não existe Endereço Electrónico na tabela para ser excluido.",
                    SystemMessage.SYSTEM_NAME, JOptionPane.WARNING_MESSAGE);
        } else {
            try {

                int linha = tableEmail.getSelectedRow();
                DefaultTableModel model = (DefaultTableModel) tableEmail.getModel();
                model.removeRow(linha);
                emailGeneric.remove(linha);
                atualizarTabela();

            } catch (ArrayIndexOutOfBoundsException e) {
                JOptionPane.showMessageDialog(this, "Selecione o Endereço Electrónico na tabela para ser excluido.",
                        SystemMessage.SYSTEM_NAME, JOptionPane.WARNING_MESSAGE);
            }
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

    private void txtEmailGenericKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEmailGenericKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            if (validateEmailExist(new ContactoEmail(txtEmailGeneric.getText()))) {
                ContactoEmailFornecedor.emailGeneric.add(new ContactoEmail(txtEmailGeneric.getText()));
                atualizarTabela();
            }
        }
    }//GEN-LAST:event_txtEmailGenericKeyPressed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tableEmail.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Não existe número de telefone na tabela para ser editado.",
                    SystemMessage.SYSTEM_NAME, JOptionPane.WARNING_MESSAGE);
        } else {
            try {

                int linha = tableEmail.getSelectedRow();
                String subString = String.valueOf(tableEmail.getValueAt(linha, 1));

                DefaultTableModel model = (DefaultTableModel) tableEmail.getModel();
                model.removeRow(linha);
                emailGeneric.remove(linha);
                atualizarTabela();
                txtEmailGeneric.setText(subString);

            } catch (ArrayIndexOutOfBoundsException e) {
                JOptionPane.showMessageDialog(this,
                        "Selecione o número de telefone na tabela para ser excluido.",
                        SystemMessage.SYSTEM_NAME, JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

