    private void configuracoes() {
        setLocationRelativeTo(null);
        this.setIconImage(new ImageIcon(getClass().
                getResource(SystemMessage.IMAGE_URL)).getImage());
        this.setTitle(SystemMessage.SYSTEM_NAME + " - Adicionando Endereços Elentrónicos");
        txtEmailGeneric.setDocument(new Expressoes.InternalClassEmail());
        atualizarTabela();
    }

    /**
     *
     */
    private void atualizarTabela() {
        DefaultTableModel model = (DefaultTableModel) tableEmail.getModel();
        model.setNumRows(0);
        int index = 1;

        for (ContactoEmail email : emailGeneric) {
            model.addRow(new Object[]{
                index++,
                email.getEmail()
            });
        }

        txtEmailGeneric.setText(null);
    }

    /**
     * ====================E-mail validation exist=====================
     *
     * @param contactoModel
     * @return
     */
    private boolean validateEmailExist(ContactoEmail email) {
        if (!Expressoes.validarEmail(email.getEmail())) {
            JOptionPane.showMessageDialog(this,
                    "Endereço Electrónico inválido",
                    SystemMessage.SYSTEM_NAME, JOptionPane.WARNING_MESSAGE);
            return false;
        } else {

            for (ContactoEmail emailGeneric1 : ContactoEmailFornecedor.emailGeneric) {
                if (emailGeneric1.getEmail().equalsIgnoreCase(email.getEmail())) {
                    JOptionPane.showMessageDialog(null,
                            "O Endereço electrónico " + email.getEmail() + ", já está na lista ",
                            SystemMessage.SYSTEM_NAME, JOptionPane.WARNING_MESSAGE);
                    return false;
                }
            }

            if (ControllerContactoFornecedor.verificarExistenciaEmailFornecedorController(email.getEmail())) {
                JOptionPane.showMessageDialog(null,
                        "Endereço electrónico indisponivel, tente outro.",
                        SystemMessage.SYSTEM_NAME,
                        JOptionPane.WARNING_MESSAGE);
                return false;
            }
        }
        return true;
    }

