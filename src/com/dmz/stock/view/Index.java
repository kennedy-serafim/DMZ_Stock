    private void validarAcesso() {
        lblPassWrong.setVisible(false);
        lblUserWrong.setVisible(false);

        if (tentativaAcesso < 3) {

            String user = txtUsuario.getText().trim();
            String pass = String.valueOf(txtPass.getPassword());

            if (!(user.isEmpty() || pass.isEmpty())) {
                System.err.println("Index "+GeradorPassword.encrypt(pass));
                int idUser = new AutenticadorController().autenticacao(user, GeradorPassword.encrypt(pass));

                if (idUser > 0) { //
                    Usuario usuario = new UsuarioController().pesquisarUsuarioPorIDDAO(idUser);
                    UsuarioSessao.setIdFuncionario(usuario.getId());
                    UsuarioSessao.setNomeFuncionario(usuario.getNome());
                    UsuarioSessao.setApelidoFuncionario(usuario.getApelido());
                    UsuarioSessao.setLogin(usuario.getUsername());
                    UsuarioSessao.setTipoUsuario(usuario.getTipoUsuario().toString());
                    UsuarioSessao.setPassword(usuario.getPassword());
                    this.dispose();

                    new Dashboard().setVisible(true);

                } else {
                    tentativaAcesso++;
                    JOptionPane.showMessageDialog(this, "Usu√°rio ou Senha inv√°lida.",
                            SystemMessage.SYSTEM_NAME,
                            JOptionPane.ERROR_MESSAGE);
                    lblPassWrong.setText("Usu√°rio ou Senha inv√°lida.");
                    lblPassWrong.setVisible(true);

                }

            } else if (user.isEmpty()) {
                lblUserWrong.setText("O Campo de Login n√£o pode estar v√°zio.");
                lblUserWrong.setVisible(true);
                txtUsuario.requestFocus();

            } else {
                lblPassWrong.setText("O Campo de Senha n√£o pode estar v√°zio.");
                lblPassWrong.setVisible(true);
                txtPass.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(this, "O Sistema est√° temporariamente bloqueado."
                    + "\nExcedeu o n√∫mero m√°ximo de tentativas",
                    SystemMessage.SYSTEM_NAME, JOptionPane.WARNING_MESSAGE);
            lblPassWrong.setText("O Sistema est√° temporariamente bloqueado");
            lblPassWrong.setVisible(true);
        }

    }

